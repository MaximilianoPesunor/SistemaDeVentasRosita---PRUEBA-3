    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try{
            String nombre_categoria = jNombreca.getText();
            String descripcion = jDesc.getText();
            
            ProcedimientoSQL conn= new ProcedimientoSQL();
            Connection con =ProcedimientoSQL.getConexion();
            
            CallableStatement conec = con.prepareCall("{call dbo.AgregarCategoria(?,?)}");
            
            conec.setString(1, nombre_categoria);
            conec.setString(2, descripcion);
            conec.execute();
            
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this,"No se puede agregar el producto");
        }
        
        try{
            int id_producto,rut_proveedor,precio_actual,stock;
            
            String nombre = jnombrePro.getText();
            
            id_producto= Integer.parseInt(jIDproducto.getText());
            rut_proveedor = Integer.parseInt(jRutp.getText());
            precio_actual = Integer.parseInt(jPrecio.getText());
            stock = Integer.parseInt(jStock.getText());
            
            ProcedimientoSQL conn= new ProcedimientoSQL();
            Connection con =ProcedimientoSQL.getConexion();
            
            CallableStatement conec = con.prepareCall("{call dbo.AgregarProducto(?,?,?,?,?)}");
            
            conec.setInt(1, id_producto);
            conec.setInt(2, rut_proveedor);
            conec.setString(3, nombre);
            conec.setInt(4,precio_actual );
            conec.setInt(5, stock);
            conec.execute();
            JOptionPane.showMessageDialog(this,"SE HA AÃ‘ADIDO EL PRODUCTO EXITOSAMENTE");
           
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"No se puede agregar el producto");
        }
        
        
        jnombrePro.setText(" ");
        jIDproducto.setText(" ");
        jRutp.setText(" ");
        jPrecio.setText(" ");
        jStock.setText(" ");
        jNombreca.setText(" ");
        jDesc.setText(" ");
        
    }//GEN-LAST:event_jButton2ActionPerformed

